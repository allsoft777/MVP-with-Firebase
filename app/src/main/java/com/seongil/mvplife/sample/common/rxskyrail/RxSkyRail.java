package com.seongil.mvplife.sample.common.rxskyrail;

import io.reactivex.Observable;
import io.reactivex.subjects.PublishSubject;

/**
 * @author seong-il, kim
 * @since 17. 4. 30
 */
public class RxSkyRail {

    // ========================================================================
    // constants
    // ========================================================================

    // ========================================================================
    // fields
    // ========================================================================
    private PublishSubject<Object> mSkyRail = PublishSubject.create();

    // ========================================================================
    // constructors
    // ========================================================================

    // ========================================================================
    // getter & setter
    // ========================================================================

    // ========================================================================
    // methods for/from superclass/interfaces
    // ========================================================================

    // ========================================================================
    // methods
    // ========================================================================
    public void send(Object o) {
        mSkyRail.onNext(o);
    }

    public Observable<Object> toObservable() {
        return mSkyRail;
    }

    public boolean hasObservers() {
        return mSkyRail.hasObservers();
    }

    // ========================================================================
    // inner and anonymous classes
    // ========================================================================
}
